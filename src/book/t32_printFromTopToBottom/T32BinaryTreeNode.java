package book.t32_printFromTopToBottom;

/**
 * @author zxf
 * @date 2018/9/13 16:02
 */
public class T32BinaryTreeNode {
    int value;
    T32BinaryTreeNode left;
    T32BinaryTreeNode right;

    public T32BinaryTreeNode() {
    }

    public T32BinaryTreeNode(int value, T32BinaryTreeNode left, T32BinaryTreeNode right) {
        this.value = value;
        this.left = left;
        this.right = right;
    }
}
